type DomInput = {
  slug: string;
  name: string;
  state: string;
  tripStyles: DestinationMetadata["tripStyles"];
  ideal: DestinationMetadata["idealDuration"];
  budgetLevel: DestinationMetadata["budgetLevel"];
  bestMonths: number[];
  travelEffort: DestinationMetadata["travelEffort"];
  weatherTags: string[];
  scores: DestinationMetadata["scores"];
  aliases: string[];
  reach: Partial<Record<string, import("../models/destination").OriginReach>>;
};

function dom(input: DomInput): DestinationMetadata {
  return {
    slug: input.slug,
    name: input.name,
    country: "India",
    state: input.state,
    canonicalLocation: `${input.name}, India`,
    tripStyles: input.tripStyles,
    idealDuration: input.ideal,
    budgetLevel: input.budgetLevel,
    bestMonths: input.bestMonths,
    travelEffort: input.travelEffort,
    weatherTags: input.weatherTags,
    scores: input.scores,
    aliases: input.aliases,
    reachFromOrigins: input.reach,
    domesticIndia: true,
  };
}

type IntlInput = Omit<DomInput, "state"> & { country: string; state?: string };

function intl(input: IntlInput): DestinationMetadata {
  return {
    slug: input.slug,
    name: input.name,
    country: input.country,
    state: input.state,
    canonicalLocation: input.state
      ? `${input.name}, ${input.state}, ${input.country}`
      : `${input.name}, ${input.country}`,
    tripStyles: input.tripStyles,
    idealDuration: input.ideal,
    budgetLevel: input.budgetLevel,
    bestMonths: input.bestMonths,
    travelEffort: input.travelEffort,
    weatherTags: input.weatherTags,
    scores: input.scores,
    aliases: input.aliases,
    reachFromOrigins: input.reach,
    domesticIndia: false,
  };
}
